package dominio;


public class Mensaje {
 private   String to,from,mensaje;
    
    
      public Mensaje(String to, String from, String mensaje) {
        this.to = to;
        this.from = from;
        this.mensaje = mensaje;
    }

    public String getTo() {
        return to;
    }

    public void setTo(String to) {
        this.to = to;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }

    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }

    @Override
    public String toString() {
        return to + "," + from + "," + mensaje;
    }
    
}
